class Musique {
  String titre;
  String auteur;
  String imagePath;
  String musicUrl;

  Musique(String title, String auteur, String imagePath, String musicUrl) {
    this.titre = title;
    this.auteur = auteur;
    this.imagePath = imagePath;
    this.imagePath = imagePath;
    this.musicUrl = musicUrl;
  }
}
